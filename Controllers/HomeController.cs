using CinemaBookingApp.Data;
using CinemaBookingApp.Models;
using Microsoft.AspNetCore.Mvc;
using System.Diagnostics;
using Microsoft.EntityFrameworkCore;

namespace CinemaBookingApp.Controllers

{
    public class HomeController : Controller
    {
        private readonly ILogger<HomeController> _logger;
        private readonly ApplicationDbContext _context;
        public HomeController(ILogger<HomeController> logger, ApplicationDbContext context)
        {
            _logger = logger;
            _context = context;
        }
        public async Task<IActionResult> Index(string searchString, DateTime? movieDate)
        {
            // ??? ????? ??????? ?? ??? ViewBag ?????? ????? ???? ???? ????? ??? ???????
            ViewBag.CurrentSearch = searchString;
            ViewBag.CurrentDate = movieDate?.ToString("yyyy-MM-dd");

            // ????? ????? ??????? ?? Query
            var moviesQuery = _context.Movies.AsQueryable();

            // 1. ??????? ????? ??? ??? ?????? (??? ?? ??????)
            if (!string.IsNullOrEmpty(searchString))
            {
                moviesQuery = moviesQuery.Where(m => m.Title.Contains(searchString));
            }

            // 2. ??????? ????? ??? ??????? ??? ???? ??? ShowTimes (??? ?? ???????)
            if (movieDate.HasValue)
            {
                // ??? ????? ??????? ???? ???? ?????? ?? ??????? ?????? ???
                var activeMovieIds = await _context.ShowTimes
                    .Where(s => s.Date.Date == movieDate.Value.Date)
                    .Select(s => s.MovieID)
                    .Distinct()
                    .ToListAsync();

                // ????? ??????? ??????? ????? ??? ??? ???????? ?? ??? ?????
                moviesQuery = moviesQuery.Where(m => activeMovieIds.Contains(m.MovieID));
            }

            var filteredMovies = await moviesQuery.ToListAsync();
            return View(filteredMovies);
        }
        public IActionResult Privacy()
        {
            return View();
        }

        [ResponseCache(Duration = 0, Location = ResponseCacheLocation.None, NoStore = true)]
        public IActionResult Error()

        {
            return View(new ErrorViewModel { RequestId = Activity.Current?.Id ?? HttpContext.TraceIdentifier });
        }
    }
}

